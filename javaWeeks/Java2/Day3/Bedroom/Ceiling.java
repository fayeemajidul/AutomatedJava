public class Ceiling {
    //Instance Field:
    int height; String paintedColor;
    //Constructor Method:
    public Ceiling(int height, String paintedColor){
        this.height = height;
        this.paintedColor = paintedColor;
    }
    //Instance Method:
    public int getHeight() {
        return height;
    }
    public String getPaintedColor() {
        return paintedColor;
    }
}
